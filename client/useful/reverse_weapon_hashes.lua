function reverseWeaponHash(hash,method)
	if not method or method == 1 then
    local name = WEAPON_HASHES_STRINGS[hash]
    if name ~= nil then
			return name
		else
			if hash ~= 0 then
				TriggerServerEvent("SentryIO_Warning", "Weapon String Hash Warning", "Weapon Hash "..hash.." returned an unknown weapon.")
			end
			return hash.." (Unknown!)"
		end
	elseif method == 2 then
    local name = WEAPON_HASHES_PICKUPS[hash]
    if name ~= nil then
        return name
    end
	end
	if hash ~= 0 then
		TriggerServerEvent("SentryIO_Warning", "Weapon Pickup Hash Warning", "Weapon Hash "..hash.." returned an unknown weapon.")
	end
   Citizen.Trace("\nError reversing weapon hash \"" .. hash .. "\". Maybe it's not been added yet?")
   return "Unknown"
end
